fn trees_in_trajectory(map: &Vec<String>, dx: usize, dy: usize) -> u32 {
    let mut column = 0;
    let mut row = 0;
    let mut count = 0;
    while row < map.len() {
        if map[row].chars().nth(column % map[row].len()).unwrap() == '#' {
            count += 1;
        }
        column += dx;
        row += dy;
    }
    count
}
